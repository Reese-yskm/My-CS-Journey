#include <bits/stdc++.h>
using namespace std;

/*
题目：1. 两数之和（Two Sum）

题目描述：
给定一个整数数组 nums 和一个整数目标值 target，
请你在该数组中找出 和为目标值 target 的那 两个 整数，
并返回它们的数组下标。

你可以假设每种输入只会对应一个答案。
但是，数组中同一个元素在答案里不能重复出现。
你可以按任意顺序返回答案。

示例：
输入：nums = [2,7,11,15], target = 9
输出：[0,1]

==================================================
思路：
这道题最经典、最高效的做法是：哈希表。
核心思想是“边遍历，边查找”。

设当前遍历到 nums[i]，那么如果它要和某个数加起来等于 target，
那个数就应该是：

    other = target - nums[i]

于是我们需要快速判断：
“other 之前有没有出现过？”

如果之前出现过，那么说明已经找到答案了。
如果之前没出现过，就把当前的 nums[i] 存进哈希表，
方便后面元素来找它。

哈希表中存的内容是：

    数值 -> 这个数对应的下标

这样当找到 other 时，就能直接得到对应下标。

==================================================
为什么不能直接排序？
因为题目要求返回“原数组下标”。

如果把 nums 直接排序，那么元素的位置会发生变化，
最后得到的是排序后的下标，不是原数组下标。

例如：
nums = [3,2,4], target = 6

排序后变成：
[2,3,4]

虽然可以找到 2 和 4，但它们在排序后的下标是 0 和 2，
而题目真正要的是原数组中的下标 1 和 2。

所以这题最自然的方法不是排序 + 二分，
而是直接使用哈希表，在不破坏原数组顺序的情况下完成查找。

==================================================
注意事项 / 收获：

1. 哈希表的键值设计
   这里用：
       unordered_map<int, int> mp;
   表示：
       数值 -> 下标

2. 一定是“先查再存”
   对于当前 nums[i]，先看 target - nums[i] 是否已经在哈希表里。
   如果在，就直接返回答案。
   如果不在，再把 nums[i] 存进去。

   这样可以避免把自己和自己配对。

3. 为什么能保证正确？
   因为题目保证“恰好只有一个答案”，
   所以一旦找到互补数，就可以直接返回。

4. 时间复杂度
   平均情况下：
       查哈希表 O(1)
       总共遍历一遍数组 O(n)
   所以总时间复杂度是：
       O(n)

5. 空间复杂度
   哈希表最多存 n 个元素：
       O(n)

==================================================
可记忆模板：
for (int i = 0; i < nums.size(); i++) {
    int other = target - nums[i];
    if (mp.count(other)) return {mp[other], i};
    mp[nums[i]] = i;
}

==================================================
*/

class Solution {
public:
    vector<int> twoSum(vector<int>& nums, int target) {
        unordered_map<int, int> mp;  // 数值 -> 下标

        for (int i = 0; i < (int)nums.size(); i++) {
            int other = target - nums[i];

            // 先查：看看互补数是否已经出现过
            if (mp.count(other)) {
                return {mp[other], i};
            }

            // 再存：把当前数和它的下标记录下来
            mp[nums[i]] = i;
        }

        return {};
    }
};
