/*
# Problem 3 成员分配

## 一、题目内容

在年度国际编程节 IPF 的晚宴上，有来自全球的 n 个代表团。

- 第 i 个代表团有 a[i] 名成员。
- 晚宴现场有 m 张圆桌。
- 第 j 张圆桌容量为 b[j]。

要求安排所有成员入座，并满足：

同一个代表团的任意两名成员不能坐在同一张桌子上。

也就是说：

如果某个代表团有 a[i] 个人，那么它必须被分配到 a[i] 张不同的桌子上，
每张桌子最多放这个代表团的 1 个人。

如果存在合法安排：
    输出 1
    然后输出 n 行。
    第 i 行输出第 i 个代表团的 a[i] 名成员分别被安排到的桌子编号。

如果不存在合法安排：
    输出 0

桌子编号从 1 开始。
如果存在多种合法方案，输出任意一种即可。


## 二、题目本质

这题不是普通的“总人数 <= 总容量”就可以。

因为还有一个额外限制：

    同一个代表团的人不能坐同一张桌子。

所以对于一个人数为 x 的代表团来说，它需要的不是“x 个座位”，而是：

    x 张当前还有空位的不同桌子。

这就说明，大代表团更难安排。

例如：
    代表团人数为 5

那么它必须找到 5 张不同的桌子。
即使总剩余座位数很多，如果只有 4 张桌子有空位，也无法安排它。


## 三、贪心思路

### 1. 先安排人数多的代表团

人数越多的代表团，要求的不同桌子数量越多，因此越难安排。

所以我们应该先处理大的代表团，避免前面的小代表团把可用桌子“打碎”，
导致后面的大代表团找不到足够多的不同桌子。


### 2. 当前代表团选择剩余容量最大的桌子

假设当前代表团有 x 个人。

那么我们从当前所有还有空位的桌子中，选择剩余容量最大的 x 张桌子，
每张桌子安排一个该代表团成员。

为什么选容量最大的桌子？

因为容量大的桌子更能承受被占用一次。
这样可以尽量保留安排后续代表团的能力。


## 四、数据结构

每次都要找到当前剩余容量最大的桌子，所以用大根堆：

    priority_queue<pair<int, int>> pq;

其中 pair 表示：

    first  = 桌子当前剩余容量
    second = 桌子编号

C++ 的 priority_queue 默认是大根堆，所以 pair 会按照 first 优先比较。
也就是说，pq.top() 就是当前剩余容量最大的桌子。


## 五、一个非常关键的实现细节

对于同一个代表团，不能把同一张桌子分配两次。

所以处理一个代表团时，不能这样：

    取出一张桌子
    容量减 1
    立刻放回堆

因为如果立刻放回堆，这张桌子仍然可能再次成为堆顶，
从而被同一个代表团再次选中，违反题意。

正确做法是：

    对当前代表团：
        先连续取出 x 张不同的桌子；
        每张桌子容量减 1；
        暂存在 temp 中；
    当前代表团全部安排完后：
        再把还有剩余容量的桌子放回堆。

这就是 temp 数组的作用。


## 六、我这次 debug 的关键错误

我原来写错的核心代码类似：

    int num = x.first;        // 当前代表团人数
    int cap = pq.top().first; // 当前桌子容量

但是后面把 num 当成了桌子容量来用：

    temp.push_back({num - 1, pos});

这是错误的。

num 的含义是：

    当前代表团还要安排多少个人

cap 的含义是：

    当前桌子还剩多少座位

每安排一个人，真正应该减少的是桌子容量 cap，而不是代表团人数 num。

正确逻辑是：

    cap--;
    if (cap > 0)
        temp.push_back({cap, pos});

另外，取出 pq.top() 后必须 pq.pop()。
否则会一直拿同一张桌子。


## 七、AC 代码
*/

#include <bits/stdc++.h>
using namespace std;

typedef long long ll;

/*
    排序规则：
        代表团人数从大到小排序。

    pair<int, int> 的含义：
        first  = 代表团人数
        second = 代表团原始编号

    为什么要保存原始编号？

        因为我们为了贪心，会改变代表团的处理顺序；
        但是最终输出时，题目要求按照原来的第 1 个、第 2 个、...代表团输出。
*/
bool cmp(const pair<int, int> &a, const pair<int, int> &b)
{
    return a.first > b.first;
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;

    while (t--)
    {
        int n, m;
        cin >> n >> m;

        /*
            a[i]：第 i 个代表团的人数
            b[j]：第 j 张桌子的容量
        */
        vector<int> a(n);
        vector<int> b(m);

        /*
            groups 存储代表团信息：
                {代表团人数, 代表团原始编号}
        */
        vector<pair<int, int>> groups(n);

        /*
            res[i] 存第 i 个代表团最终被安排到的桌子编号。
            最后按 i = 0 到 n - 1 输出即可。
        */
        vector<vector<int>> res(n);

        /*
            大根堆，维护当前所有还有空位的桌子。

            pair<int, int>：
                first  = 当前桌子剩余容量
                second = 桌子编号，使用 1-indexed
        */
        priority_queue<pair<int, int>> pq;

        /*
            读入每个代表团的人数。
            同时建立 groups 数组。
        */
        for (int i = 0; i < n; i++)
        {
            cin >> a[i];

            groups[i].first = a[i]; // 代表团人数
            groups[i].second = i;   // 原始编号
        }

        /*
            读入每张桌子的容量。
            只有容量大于 0 的桌子才放入堆。

            如果容量为 0，说明这张桌子不能坐人，
            不能把它算作“当前可用桌子”。
        */
        for (int i = 0; i < m; i++)
        {
            cin >> b[i];

            if (b[i] > 0)
            {
                pq.push({b[i], i + 1});
            }
        }

        /*
            人数多的代表团优先安排。
        */
        sort(groups.begin(), groups.end(), cmp);

        bool ok = true;

        /*
            按照人数从大到小，依次安排代表团。
        */
        for (auto &x : groups)
        {
            int num = x.first; // 当前代表团人数
            int id = x.second; // 当前代表团原始编号

            /*
                当前代表团有 num 个人，
                必须安排到 num 张不同的桌子上。

                pq.size() 表示当前还有空位的桌子数量。
                如果当前可用桌子数不足 num，则不可能安排。
            */
            if (num > (int)pq.size())
            {
                ok = false;
                break;
            }

            /*
                temp 暂存当前代表团使用过的桌子。

                注意：
                    这些桌子不能立刻放回 pq。
                    因为同一个代表团不能重复使用同一张桌子。

                所以当前代表团全部安排完之后，再统一放回。
            */
            vector<pair<int, int>> temp;

            /*
                给当前代表团的每个成员分配桌子。
                一共要取 num 张不同的桌子。
            */
            while (num--)
            {
                /*
                    取出当前剩余容量最大的桌子。
                */
                auto cur = pq.top();
                pq.pop();

                int cap = cur.first;  // 当前桌子剩余容量
                int pos = cur.second; // 当前桌子编号

                /*
                    当前代表团的一个成员坐到 pos 号桌子。
                */
                res[id].push_back(pos);

                /*
                    这张桌子被占用了一个位置，
                    所以容量减少 1。
                */
                cap--;

                /*
                    如果这张桌子还有剩余容量，
                    那它以后还能安排其他代表团的人。
                    但是不能现在立刻放回堆，
                    要先放入 temp。
                */
                if (cap > 0)
                {
                    temp.push_back({cap, pos});
                }
            }

            /*
                当前代表团已经全部安排完成。

                现在再把这些还有剩余容量的桌子放回堆，
                供后面的代表团使用。
            */
            for (auto &u : temp)
            {
                pq.push(u);
            }
        }

        /*
            如果无法安排，输出 0。
        */
        if (!ok)
        {
            cout << "0\n";
        }
        else
        {
            /*
                如果可以安排，先输出 1。
            */
            cout << "1\n";

            /*
                按照代表团原始顺序输出方案。

                注意：
                    每一行内部的桌子编号顺序任意。
                    所以不需要和样例完全一样。
            */
            for (int i = 0; i < (int)res.size(); i++)
            {
                for (int j = 0; j < (int)res[i].size(); j++)
                {
                    if (j)
                    {
                        cout << " ";
                    }

                    cout << res[i][j];
                }

                cout << "\n";
            }
        }
    }

    return 0;
}

/*
## 八、复杂度分析

设：
    n 为代表团数量
    m 为桌子数量
    S 为所有代表团总人数之和

排序代表团：
    O(n log n)

每安排一个人，需要从堆中取出一张桌子，之后可能再放回。
每次堆操作是 O(log m)。

所以总复杂度大约是：

    O(n log n + S log m)

在本题数据范围下可以接受。


## 九、这道题的收获

### 1. 这题不是差分

一开始容易想到“对桌子容量减一”，但它不是标准差分。

因为差分一般处理的是：

    对连续区间 [l, r] 统一加减

但这题中，一个代表团要选择的是若干张不同桌子，
这些桌子编号不一定连续。

所以更适合用：

    贪心 + 优先队列


### 2. 大代表团要先安排

人数越多的代表团，越需要更多张不同桌子，因此越难安排。

所以应该优先安排人数多的代表团。


### 3. 每次选择剩余容量最大的桌子

对于当前代表团，要选择当前剩余容量最大的若干张桌子。

这样可以尽量让容量大的桌子承担更多压力，
保留后续安排的空间。


### 4. 同一个代表团不能重复使用同一张桌子

这是代码里最容易错的点。

错误写法：

    pop 一张桌子
    cap--
    立刻 push 回堆

这样同一张桌子可能被同一个代表团再次选到。

正确写法：

    当前代表团先把需要的桌子全部 pop 出来
    放入 temp
    当前代表团安排完后，再统一 push 回堆


### 5. 变量语义一定要分清

这次 debug 最大的问题不是变量名拼错，
而是变量含义混了。

    num：当前代表团还要安排多少个人
    cap：当前桌子还剩多少容量

每安排一个人：

    num 用来控制循环次数
    cap 才是要减少的资源

错误写法：

    temp.push_back({num - 1, pos});

正确写法：

    cap--;
    if (cap > 0)
        temp.push_back({cap, pos});


### 6. priority_queue 的 top 用完后要 pop

如果只写：

    pq.top()

但不写：

    pq.pop()

那么每次看到的都是同一张桌子。

这会导致同一个代表团被反复安排到同一张桌子上。


## 十、一句话总结

这题的核心模型是：

    先安排最难安排的大代表团；
    每个代表团选择当前剩余容量最大的若干张不同桌子；
    用优先队列维护桌子剩余容量。

写模拟时最重要的是分清：

    代表团人数 num 是循环控制变量；
    桌子容量 cap 才是被消耗的资源。
*/