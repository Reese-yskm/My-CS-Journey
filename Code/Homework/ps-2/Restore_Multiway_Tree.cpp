/**
 * @file Restore_Multiway_Tree.cpp
 * @brief 前序 + 后序恢复多叉树，并输出每层最右节点
 *
 * 【题目内容】
 * 给你一棵多叉树的前序遍历和后序遍历，要求你把这棵多叉树恢复出来，
 * 然后输出这棵树每一层最右边的节点。
 *
 * 多叉树遍历定义：
 *
 * 1. 前序遍历：
 *    到达某个节点时，先访问自己，
 *    再按从左到右顺序遍历它的所有孩子子树。
 *
 * 2. 后序遍历：
 *    先按从左到右顺序遍历完所有孩子子树，
 *    最后再访问自己。
 *
 * --------------------------------------------------
 * 【题目目标拆解】
 *
 * 这题看起来要求输出“每层最右节点”，
 * 但真正的难点其实在第一步：
 *
 *     如何根据前序 + 后序恢复多叉树结构
 *
 * 一旦树恢复出来了，
 * 后面只需要做一次层序遍历（BFS），
 * 每层取最后一个节点即可。
 *
 * 所以整题分成两步：
 *
 * Step 1. 恢复多叉树的父子关系
 * Step 2. BFS 输出每层最后一个节点
 *
 * --------------------------------------------------
 * 【为什么这题难】
 *
 * 这题比“前序 + 中序构造二叉树”更难，
 * 因为这里不是二叉树，而是多叉树。
 *
 * 二叉树的特点是：
 * - 每个根最多只有左子树和右子树
 * - 可以一刀切成两部分
 *
 * 但多叉树不是。
 *
 * 对于当前根来说，它下面可能有：
 * - 1 个孩子
 * - 2 个孩子
 * - 5 个孩子
 * - ...
 *
 * 所以当前根的孩子区间不是“切成两块”，
 * 而是要：
 *
 *     一个一个地把孩子子树切出来
 *
 * 这就是本题最本质的难点。
 *
 * --------------------------------------------------
 * 【这题最核心的两个性质】
 *
 * 性质 1：
 * 前序里，当前根后面的第一个未处理节点，
 * 一定是当前根的下一个孩子。
 *
 * 原因：
 * 前序遍历是先访问根，再立刻进入第一个孩子子树。
 *
 * --------------------------------------------------
 *
 * 性质 2：
 * 一棵子树在后序遍历中一定是一整块，
 * 并且这整块的最后一个节点就是这棵子树的根。
 *
 * 原因：
 * 后序遍历是先遍历完整棵子树的所有后代，
 * 最后访问这棵子树的根。
 *
 * --------------------------------------------------
 *
 * 所以这题最重要的一句话是：
 *
 *     前序负责告诉我“下一个孩子是谁”，
 *     后序负责告诉我“这个孩子子树到哪里结束”。
 *
 * --------------------------------------------------
 * 【树怎么存】
 *
 * 这题最自然的存法不是复杂的指针节点，
 * 而是直接用“孩子数组”表示：
 *
 *     children[u]
 *
 * 表示节点 u 的所有孩子，按从左到右顺序存放。
 *
 * 例如如果恢复出来：
 *     1 -> 2, 6
 *     2 -> 3, 4, 5
 *
 * 那就存成：
 *
 *     children[1] = {2, 6}
 *     children[2] = {3, 4, 5}
 *
 * 这样后面 BFS 就很好做。
 *
 * --------------------------------------------------
 * 【递归函数的定义】
 *
 * 定义：
 *
 *     build(preL, preR, postL, postR)
 *
 * 表示：
 *
 *     用前序区间 pre[preL..preR]
 *     和后序区间 post[postL..postR]
 *     恢复这一整棵子树的父子关系，
 *     并把边记入 children。
 *
 * 注意：
 * - 当前子树根是 pre[preL]
 * - 也一定等于 post[postR]
 *
 * 这个函数不需要返回值，
 * 因为它的任务不是返回节点，
 * 而是直接把父子关系写进全局 children。
 *
 * --------------------------------------------------
 * 【递归边界】
 *
 * 如果：
 *
 *     preL >= preR
 *
 * 说明当前区间里至多只有一个节点，
 * 即当前子树只有根，没有孩子。
 *
 * 所以直接 return。
 *
 * --------------------------------------------------
 * 【当前根要做什么】
 *
 * 假设当前根：
 *
 *     root = pre[preL]
 *
 * 那么：
 *
 * - 前序中 pre[preL+1 .. preR] 这一段，
 *   就是 root 的所有孩子子树拼起来的内容
 *
 * - 后序中 post[postL .. postR-1] 这一段，
 *   也是 root 的所有孩子子树拼起来的内容
 *
 * 所以当前根要做的事是：
 *
 *     在这两段“孩子区间”中，
 *     一个一个地切出每个孩子子树
 *
 * --------------------------------------------------
 * 【为什么要用 while 循环，而不是固定递归两次】
 *
 * 因为当前根不是只有两个子树，
 * 而是可能有很多孩子。
 *
 * 所以这里不能像二叉树那样写成：
 * - 递归左子树
 * - 递归右子树
 *
 * 而必须写成：
 *
 *     while (...) {
 *         找下一个孩子子树
 *         递归处理这个孩子子树
 *     }
 *
 * --------------------------------------------------
 * 【递归过程中的两个指针】
 *
 * 设：
 *
 *     i = preL + 1
 *     j = postL
 *
 * 含义：
 *
 * - i 指向当前前序中，还没处理到的第一个孩子位置
 * - j 指向当前后序中，还没消耗掉的第一个位置
 *
 * 接下来每轮 while 循环都在做：
 *
 *     切出当前根的一个孩子子树
 *
 * --------------------------------------------------
 * 【每轮 while 循环做的事】
 *
 * Step 1.
 * 当前前序里第一个未处理点：
 *
 *     child = pre[i]
 *
 * 根据前序性质，它一定是当前根 root 的下一个孩子。
 *
 * 所以先记录：
 *
 *     children[root].push_back(child)
 *
 * --------------------------------------------------
 *
 * Step 2.
 * 去后序中找到 child 的位置，记为 k。
 *
 * 因为子树在后序中是一整块，
 * 且最后一个点就是这棵子树的根，
 * 所以：
 *
 *     child 子树在后序中的区间就是 [j .. k]
 *
 * --------------------------------------------------
 *
 * Step 3.
 * 这个孩子子树的节点数就是：
 *
 *     len = k - j + 1
 *
 * --------------------------------------------------
 *
 * Step 4.
 * 既然这个孩子子树长度是 len，
 * 那它在前序中的对应区间一定也是长度为 len 的一段，
 * 而且从 pre[i] 开始。
 *
 * 所以这个孩子子树在前序中的区间就是：
 *
 *     [i .. i + len - 1]
 *
 * --------------------------------------------------
 *
 * Step 5.
 * 递归处理这个孩子子树：
 *
 *     build(i, i + len - 1, j, k)
 *
 * --------------------------------------------------
 *
 * Step 6.
 * 当前这个孩子子树处理完后，
 * 前序和后序的指针都要向后跳过这一整块：
 *
 *     i = i + len
 *     j = k + 1
 *
 * 然后继续找当前根的下一个孩子。
 *
 * --------------------------------------------------
 * 【while 什么时候结束】
 *
 * 当前根的所有孩子都在前序区间：
 *
 *     pre[preL+1 .. preR]
 *
 * 当这些孩子子树全部切完后，
 * 指针 i 就会越过 preR。
 *
 * 所以循环条件自然写成：
 *
 *     while (i <= preR)
 *
 * --------------------------------------------------
 * 【递归函数骨架总结】
 *
 * 1. 如果当前区间只有根，直接 return
 * 2. 当前根是 pre[preL]
 * 3. 用 i 和 j 指向当前未处理的孩子区间
 * 4. while 循环中：
 *    - pre[i] 是下一个孩子
 *    - 去后序里找到它的位置 k
 *    - len = k - j + 1
 *    - 递归处理这个孩子子树
 *    - i, j 往后跳
 *
 * --------------------------------------------------
 * 【恢复完树之后怎么取每层最右节点】
 *
 * 既然已经有了 children[u]，
 * 后面只需要做层序遍历 BFS。
 *
 * 做法：
 *
 * 1. 根节点入队
 * 2. 每次固定当前层大小 sz
 * 3. 这一层依次出队 sz 个节点
 * 4. 本层最后一个出队的节点，就是本层最右边节点
 * 5. 同时把这些节点的孩子按顺序入队
 *
 * 因为 children[u] 本身就是从左到右顺序，
 * 所以下一层进入队列的顺序也是从左到右。
 *
 * 所以本层最后一个出队节点，
 * 就恰好是本层最右边节点。
 *
 * --------------------------------------------------
 * 【样例理解】
 *
 * 样例：
 *
 * 前序：
 *     1 2 3 4 5 6
 *
 * 后序：
 *     3 4 5 2 6 1
 *
 * 恢复得到：
 *
 *     1 -> 2, 6
 *     2 -> 3, 4, 5
 *
 * 按层就是：
 *
 * 第1层：1
 * 第2层：2 6
 * 第3层：3 4 5
 *
 * 所以答案：
 *
 *     1 6 5
 *
 * --------------------------------------------------
 * 【本题最容易错的地方】
 *
 * 1. 只找出第一个孩子，没有继续用 while 找完所有孩子
 *
 * 2. 误以为“剩余区间”是一整棵子树
 *    实际上它可能只是当前根后续多个孩子子树拼起来的内容
 *
 * 3. 子树长度 len 漏写 +1
 *    正确是：
 *        len = k - j + 1
 *
 * 4. BFS 时取错答案
 *    题目要的是“本层最后一个节点”，
 *    不是“本层最后一个节点的最后一个孩子”
 *
 * 5. children 的定义方式写错
 *    如果写成 vector<vector<int>> children(n)，
 *    但 n 还没读入，会直接出问题
 *
 * --------------------------------------------------
 * 【这题最精髓的地方】
 *
 * 我觉得这题真正精髓的不是 BFS，
 * 而是这个递归结构：
 *
 *     当前根不是把区间切成左右两半，
 *     而是把孩子区间循环切成若干个连续的孩子子树块。
 *
 * 这和二叉树构造题的思维很不一样，
 * 也是这题最值得反复回味的部分。
 *
 * --------------------------------------------------
 * 【一句话总结】
 *
 * 这题的本质是：
 *
 *     利用“前序认孩子，后序定边界”这两个性质，
 *     在当前根的孩子区间里，用 while 循环一个一个切出孩子子树，
 *     恢复多叉树结构；恢复完后再 BFS 取每层最后一个节点。
 */
#include <bits/stdc++.h>
using namespace std;

typedef long long ll;

int n;

// pre[i]：多叉树的前序遍历序列
// post[i]：多叉树的后序遍历序列
int pre[50005];
int post[50005];

// children[u]：节点 u 的所有孩子，按从左到右的顺序存放
// 这就是恢复出来的多叉树结构
vector<int> children[50005];

// postpos[x]：节点 x 在后序遍历 post 中的位置
// 用它可以 O(1) 找到某个孩子子树在后序中的结束位置
unordered_map<int, int> postpos;

/**
 * @brief 根据前序区间和后序区间恢复当前子树
 *
 * @param preL   当前子树在前序遍历中的左端点
 * @param preR   当前子树在前序遍历中的右端点
 * @param postL  当前子树在后序遍历中的左端点
 * @param postR  当前子树在后序遍历中的右端点
 *
 * 函数含义：
 * 用 pre[preL..preR] 和 post[postL..postR] 这两段遍历序列，
 * 恢复这一整棵子树的父子关系，并写入 children。
 *
 * 当前子树的根：
 * - 前序第一个节点：pre[preL]
 * - 后序最后一个节点：post[postR]
 *
 * 核心思想：
 * - 前序负责告诉我们“当前根的下一个孩子是谁”
 * - 后序负责告诉我们“这个孩子子树到哪里结束”
 */
void build(int preL, int preR, int postL, int postR)
{
    // 如果当前区间中只有一个节点，说明它是叶子节点，没有孩子
    // preL > preR 理论上表示空区间，写 >= 更稳
    if (preL >= preR)
    {
        return;
    }

    // 当前子树的根节点
    int root = pre[preL];

    // i 指向前序中当前还没有处理的第一个孩子位置
    // 因为 pre[preL] 是根，所以孩子区间从 preL + 1 开始
    int i = preL + 1;

    // j 指向后序中当前还没有消耗的起始位置
    // 后序中 post[postR] 是根，所以孩子区间是 postL..postR-1
    int j = postL;

    // 当前根 root 可能有多个孩子
    // 所以不能像二叉树那样只切左、右两棵子树
    // 而是要用 while 循环，一个一个切出 root 的孩子子树
    while (i <= preR)
    {
        // 在前序中，当前未处理的第一个节点就是 root 的下一个孩子
        int child = pre[i];

        // 记录父子关系：child 是 root 的一个孩子
        // 因为 i 按前序从左到右推进，所以 push 的顺序就是孩子的左右顺序
        children[root].push_back(child);

        // 在后序中找到 child 的位置
        // 因为一棵子树在后序中是一整块，并且最后一个节点是这棵子树的根
        // 所以 post[j..k] 就是 child 这棵子树的后序区间
        int k = postpos[child];

        // child 子树的节点个数
        // 后序区间 [j, k] 的长度就是 k - j + 1
        int len = k - j + 1;

        // 既然 child 子树有 len 个节点，
        // 那么它在前序中也占据从 i 开始的 len 个位置：
        // pre[i..i+len-1]
        //
        // 递归恢复 child 这棵子树
        build(i, i + len - 1, j, k);

        // 当前 child 子树已经处理完
        // 前序指针跳过这整棵子树
        i = i + len;

        // 后序指针也跳过这整棵子树
        j = k + 1;
    }
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    cin >> n;

    // 读入前序遍历
    for (int i = 0; i < n; i++)
    {
        cin >> pre[i];
    }

    // 读入后序遍历，并记录每个节点在后序中的位置
    // 后面需要通过 postpos[child] 快速确定 child 子树的结束位置
    for (int i = 0; i < n; i++)
    {
        cin >> post[i];
        postpos[post[i]] = i;
    }

    // 根据整段前序和整段后序恢复整棵多叉树
    build(0, n - 1, 0, n - 1);

    // ans 存储每一层最右边的节点
    vector<int> ans;

    // 用 BFS 层序遍历恢复出来的多叉树
    queue<int> q;

    // 整棵树的根是前序遍历的第一个节点
    q.push(pre[0]);

    while (!q.empty())
    {
        // 当前层的节点数量
        int size = q.size();

        // 依次处理当前层的所有节点
        for (int i = 0; i < size; i++)
        {
            int cur = q.front();
            q.pop();

            // 当前层最后一个出队的节点，就是这一层最右边的节点
            if (i == size - 1)
            {
                ans.push_back(cur);
            }

            // 按从左到右的顺序，把 cur 的所有孩子加入队列
            // 这样下一层出队时，也会保持从左到右的顺序
            for (auto x : children[cur])
            {
                q.push(x);
            }
        }
    }

    // 输出每层最右边的节点
    for (int i = 0; i < ans.size(); i++)
    {
        cout << ans[i] << " ";
    }

    return 0;
}