/**
 * ======================================================================================
 * @file    Recursive_Sequence_Query.cpp
 * @author  Reese_fan (NJU-CS)
 * @date    2026-04-25
 * @brief   递归构造序列反查：倍增长度 + 位置反推，不展开字符串
 * ======================================================================================
 *
 * 【题目模型】
 *
 * 给定初始 n 个字符串：
 *
 *     A0 = [s1, s2, s3, ..., sn]
 *
 * 之后按如下规则不断构造新序列：
 *
 *     A = A + A的最后一个元素 + A的前 len-1 个元素
 *
 * 更准确地说，如果当前序列为：
 *
 *     A = [a1, a2, ..., am]
 *
 * 那么下一层序列为：
 *
 *     A' = [a1, a2, ..., am, am, a1, a2, ..., a_{m-1}]
 *
 * 也就是：
 *
 *     A' = A + rotate_right(A)
 *
 * 其中 rotate_right(A) 表示把 A 右移一位：
 *
 *     [a1, a2, ..., am] 右移一位后变成 [am, a1, a2, ..., a_{m-1}]
 *
 * 题目给出若干询问 q_i，要求输出最终构造序列中第 q_i 个位置对应的字符串。
 *
 * ======================================================================================
 *
 * 【核心思想：不构造序列，只反推位置】
 *
 * 由于每一轮构造后长度翻倍：
 *
 *     n, 2n, 4n, 8n, ...
 *
 * 如果真的构造整个序列，长度会指数爆炸，必然超时或爆内存。
 *
 * 因此本题的核心思想是：
 *
 *     不关心整个序列长什么样，只关心第 q 个位置来自上一层的哪个位置。
 *
 * 每一层序列可以分为两半：
 *
 *     前半段：上一层原序列
 *     后半段：上一层右移一位后的序列
 *
 * 也就是说，若当前层长度为 len，上一层长度为 half = len / 2，则：
 *
 *     当前层 [1, half]       来自上一层 [1, half]
 *     当前层 [half+1, len]   来自上一层右移一位
 *
 * 因此可以把 q 一层一层反推回原始长度 n 的区间。
 *
 * ======================================================================================
 *
 * 【位置映射规则】
 *
 * 设当前层长度为 len，上一层长度为：
 *
 *     half = len / 2
 *
 * 对于当前位置 pos：
 *
 * --------------------------------------------------------------------------------------
 *
 * 情况 1：pos 在前半段
 *
 *     if (pos <= half)
 *
 * 前半段就是上一层原样复制，所以：
 *
 *     pos 不变
 *
 * --------------------------------------------------------------------------------------
 *
 * 情况 2：pos 在后半段
 *
 *     if (pos > half)
 *
 * 先计算它在后半段中的相对位置：
 *
 *     r = pos - half
 *
 * 后半段是上一层右移一位：
 *
 *     [上一层第 half 个, 上一层第 1 个, 上一层第 2 个, ..., 上一层第 half-1 个]
 *
 * 所以映射关系为：
 *
 *     r = 1  -> 上一层第 half 个
 *     r = 2  -> 上一层第 1 个
 *     r = 3  -> 上一层第 2 个
 *     ...
 *     r = k  -> 上一层第 k-1 个
 *
 * 因此代码写成：
 *
 *     if (r == 1)
 *         pos = half;
 *     else
 *         pos = r - 1;
 *
 * 这是本题最关键、最容易写错的地方。
 *
 * ======================================================================================
 *
 * 【算法流程】
 *
 * 对于每个查询 pos：
 *
 *     1. 从原始长度 len = n 开始，不断翻倍，直到 len >= pos
 *
 *            while (len < pos)
 *                len *= 2;
 *
 *        此时 len 表示“包含 pos 的最小构造层长度”。
 *
 *     2. 从这一层开始反推：
 *
 *            while (len > n)
 *
 *        每次根据 pos 在前半段还是后半段，把 pos 映射回上一层。
 *
 *     3. 当 len == n 时，pos 已经回到原始序列下标。
 *
 *     4. 输出初始字符串数组中的第 pos 个字符串。
 *
 * ======================================================================================
 *
 * 【复杂度分析】
 *
 * 每次 len 都除以 2，因此每个询问的复杂度为：
 *
 *     O(log q_i)
 *
 * 若有 Q 次询问，总复杂度为：
 *
 *     O(Q log q_max)
 *
 * 空间复杂度：
 *
 *     O(n)
 *
 * 只需要保存初始 n 个字符串，不需要保存构造后的巨大序列。
 *
 * ======================================================================================
 *
 * 【工程避坑指南】
 *
 * 1. pos 和 len 必须使用 long long
 *
 *    因为位置 q_i 可能很大，长度不断翻倍，int 容易溢出。
 *
 *
 * 2. 不要真的构造序列
 *
 *    本题的序列长度指数增长，构造完整序列会爆内存。
 *
 *
 * 3. 不要把 while 条件写死
 *
 *    错误：
 *
 *        while (pos > 4)
 *
 *    正确：
 *
 *        while (len > n)
 *
 *    原始长度是题目输入的 n，不一定是 4。
 *
 *
 * 4. 每反推一层，len 必须减半
 *
 *    错误思路：
 *
 *        只改变 pos，不改变当前层长度。
 *
 *    正确做法：
 *
 *        len = half;
 *
 *    因为 pos 已经被映射到了上一层。
 *
 *
 * 5. 后半段第一个位置要特殊处理
 *
 *    后半段第一个位置来自上一层最后一个位置。
 *
 *    所以：
 *
 *        if (r == 1) pos = half;
 *
 *    不能直接写：
 *
 *        pos = r - 1;
 *
 *    否则 r = 1 时会得到 pos = 0，导致下标错误。
 *
 *
 * 6. 建议使用 1-based 下标
 *
 *    查询“第 q 个”天然是从 1 开始。
 *
 *    使用：
 *
 *        vector<string> name(n + 1);
 *
 *    可以避免频繁写 name[pos - 1]，减少下标偏移错误。
 *
 * ======================================================================================
 */

#include <bits/stdc++.h>
using namespace std;

typedef long long ll;

/**
 * @brief 将构造后序列中的位置 pos 反推回原始序列中的位置
 * @param pos 查询的位置，从 1 开始
 * @param n   原始序列长度
 * @return    pos 在原始序列中对应的位置
 */
ll trace_position(ll pos, ll n)
{
    // 1. 找到第一个长度 >= pos 的构造层
    ll len = n;

    while (len < pos)
    {
        len *= 2;
    }

    // 2. 从当前层不断向上一层反推
    while (len > n)
    {
        ll half = len / 2;

        // 如果在前半段，pos 不变
        if (pos <= half)
        {
            // do nothing
        }
        else
        {
            // 如果在后半段，需要根据 rotate_right 的规则映射
            ll r = pos - half;

            if (r == 1)
            {
                pos = half;
            }
            else
            {
                pos = r - 1;
            }
        }

        // 已经反推到上一层，当前层长度减半
        len = half;
    }

    return pos;
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n, q;
    cin >> n >> q;

    vector<string> name(n + 1);

    for (int i = 1; i <= n; i++)
    {
        cin >> name[i];
    }

    while (q--)
    {
        ll pos;
        cin >> pos;

        ll idx = trace_position(pos, n);

        cout << name[idx] << '\n';
    }

    return 0;
}

/**
 * ======================================================================================
 * 【样例推演】
 *
 * 假设初始序列为：
 *
 *     A0 = [a, b, c, d]
 *
 * 则 n = 4。
 *
 * 第一层构造：
 *
 *     A1 = [a, b, c, d, d, a, b, c]
 *
 * 其中：
 *
 *     前 4 个是原序列：
 *
 *         [a, b, c, d]
 *
 *     后 4 个是原序列右移一位：
 *
 *         [d, a, b, c]
 *
 * --------------------------------------------------------------------------------------
 *
 * 查询 pos = 6：
 *
 *     当前 len = 8，half = 4
 *
 *     pos = 6 > 4，说明在后半段。
 *
 *     r = 6 - 4 = 2
 *
 *     后半段第 2 个来自上一层第 1 个。
 *
 *     所以：
 *
 *         pos = r - 1 = 1
 *
 *     现在 len = 4，已经回到原始序列。
 *
 *     输出：
 *
 *         name[1] = a
 *
 * --------------------------------------------------------------------------------------
 *
 * 查询 pos = 5：
 *
 *     当前 len = 8，half = 4
 *
 *     pos = 5 > 4，说明在后半段。
 *
 *     r = 5 - 4 = 1
 *
 *     后半段第 1 个来自上一层最后一个。
 *
 *     所以：
 *
 *         pos = half = 4
 *
 *     输出：
 *
 *         name[4] = d
 *
 * ======================================================================================
 *
 * 【和二分思想的关系】
 *
 * 这道题不是传统意义上的“二分答案”，但它有明显的二分区间思想：
 *
 *     每一层都分成前半段和后半段。
 *
 * 查询一个位置时，只需要判断它落在哪一半，然后映射回上一层。
 *
 * 所以它可以理解为：
 *
 *     倍增构造 + 二分定位 + 位置反推
 *
 * ======================================================================================
 *
 * 【常见变形】
 *
 * 1. A' = A + A
 *
 *    后半段映射：
 *
 *        if (pos > half)
 *            pos -= half;
 *
 *
 * 2. A' = A + reverse(A)
 *
 *    后半段映射：
 *
 *        ll r = pos - half;
 *        pos = half - r + 1;
 *
 *
 * 3. A' = A + rotate_right(A)
 *
 *    本题映射：
 *
 *        ll r = pos - half;
 *        if (r == 1) pos = half;
 *        else pos = r - 1;
 *
 *
 * 4. A' = A + rotate_left(A)
 *
 *    后半段映射：
 *
 *        ll r = pos - half;
 *        if (r == half) pos = 1;
 *        else pos = r + 1;
 *
 * ======================================================================================
 *
 * 【一句话总结】
 *
 * 本题的核心是：
 *
 *     序列指数增长时，不要展开序列；
 *     先倍增找到包含查询位置的层；
 *     再根据构造规则，把位置一层层反推回原始序列。
 *
 * 对于本题：
 *
 *     后半段 = 上一层右移一位
 *
 * 所以：
 *
 *     后半段第 1 个映射到上一层最后一个；
 *     后半段第 r 个映射到上一层第 r-1 个。
 *
 * ======================================================================================
 */