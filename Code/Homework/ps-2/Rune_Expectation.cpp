/**
 * @file Rune_Expectation.cpp
 * @brief 期望 + 策略优化 + 状态压缩 - 强化符文问题
 *
 * 【题目描述】
 * 一开始有 3 个强化符文，每个符文有一个互不相同的价值。
 * 此外还有其余 n-3 个互不相同的符文放在“未出现的符文池”中。
 *
 * 你可以进行两类操作：
 *
 * 1. 刷新
 *    对于初始的每个符文，你都有一次刷新机会。
 *    如果刷新某个初始符文，那么系统会从所有还未出现的符文中
 *    等概率随机抽取一个新符文来替换它。
 *    每个初始符文最多只能被刷新一次。
 *
 * 2. 选择
 *    在操作结束后，你从当前 3 个符文中选择一个，获得其价值。
 *    一局游戏中只能选择一次，因此目标是让最终 3 个符文中的最大值期望尽可能大。
 *
 * 题目要求：在给定初始 3 个符文和剩余符文价值的情况下，
 * 求在最优操作策略下，最终获得的符文价值的期望。
 *
 * 输出答案保留三位小数。
 *
 * --------------------------------------------------
 * 【这题真正难在哪】
 *
 * 这题难点不在代码，而在于先把问题建模清楚。
 * 一旦最优策略和期望公式推出来，后面的实现本质上就是把数学表达式翻译成代码。
 *
 * 也就是说：
 * - 难点在“想”
 * - 不是难在“写”
 *
 * --------------------------------------------------
 * 【第一步关键结论：初始最小的两个一定会被刷新】
 *
 * 设初始 3 个符文排序后为：
 *
 *     a1 <= a2 <= a3
 *
 * 那么为了使最终选到的最大值期望尽可能大，
 * a1 和 a2 一定会被刷新。
 *
 * 为什么？
 * - 因为最终只能从 3 个符文中选一个，也就是只看最大值。
 * - a1 和 a2 一开始都不是最大值。
 * - 把它们刷新成随机新值，最差也不会比“保留原值”更糟，
 *   因为原来的 a1、a2 本来就不可能被选中。
 *
 * 所以：
 * 初始 3 个里，最小的两个一定要刷掉。
 *
 * 这是整题最关键的第一刀。
 *
 * --------------------------------------------------
 * 【第二步关键结论：前两次刷新后，只需要关心“较大值是谁”】
 *
 * 设剩余符文池中共有 m = n - 3 个值，
 * 排序后记为：
 *
 *     b1 < b2 < ... < bm
 *
 * 刷掉 a1、a2 后，会从这 m 个值中不放回抽出两个。
 * 假设抽出的两个值中较大者是 bj。
 *
 * 那么此时当前 3 个符文里的最大值就是：
 *
 *     max(a3, bj)
 *
 * 因为另一张被抽出来的较小值一定不如 bj 大。
 *
 * 所以：
 * 前两次刷新后的局面，其实只和“较大值 bj 是谁”有关，
 * 而不必关心较小那张到底是谁。
 *
 * 这就是本题最漂亮的“状态压缩”。
 *
 * --------------------------------------------------
 * 【第三步关键结论：第三个 a3 刷不刷新，要比较一个期望】
 *
 * 当前已知前两次刷新后的较大值是 bj。
 *
 * 此时有两种选择：
 *
 * 1. 不刷新 a3
 *    最终收益为：
 *
 *        max(a3, bj)
 *
 * 2. 刷新 a3
 *    那么会从剩余的 m-2 个值里再随机抽一个新值 R，
 *    最终收益变成：
 *
 *        max(bj, R)
 *
 * 因此只需要比较：
 *
 *        max(a3, bj)
 *    和  E[max(bj, R)]
 *
 * 后者就是“是否值得刷掉 a3”的关键。
 *
 * --------------------------------------------------
 * 【第四步：定义 Gj】
 *
 * 定义：
 *
 *     Gj = E[max(bj, R)]
 *
 * 其中 R 是在“前两次已经抽掉一个比 bj 小的值和 bj 本身之后”，
 * 从剩余 m-2 个值中等概率随机抽出的一个值。
 *
 * 那么 Gj 可以推成：
 *
 *     Gj = [ (j-2) * bj + sum_{k=j+1}^{m} bk ] / (m-2)
 *
 * 解释：
 * - 剩余里比 bj 小的值还有 j-2 个，
 *   对它们取 max(bj, ·) 的结果都是 bj
 * - 比 bj 大的值有 bk (k=j+1...m)，取 max 后贡献它们自己
 *
 * 因此：
 *
 *     Gj = ((j-2)*bj + 右侧所有更大值之和) / (m-2)
 *
 * --------------------------------------------------
 * 【第五步：在“较大值是 bj”的条件下，最优收益 Fj】
 *
 * 若前两次刷新后的较大值是 bj，
 * 那么最终最优收益为：
 *
 *     Fj = max(a3, Gj)
 *
 * 这里有个小细节：
 * 原本不刷时收益是 max(a3, bj)，刷时收益是 Gj。
 *
 * 但由于 Gj >= bj，所以：
 *
 *     max( max(a3, bj), Gj ) = max(a3, Gj)
 *
 * 因而可以直接写成 Fj = max(a3, Gj)。
 *
 * --------------------------------------------------
 * 【第六步：较大值恰好为 bj 的概率】
 *
 * 前两次刷新相当于从 m 个值里不放回抽两个。
 *
 * 如果两次抽到的较大值恰好为 bj，
 * 那么另一个值必须从 b1 ... b(j-1) 中选一个，共 j-1 种。
 * 两次抽取有顺序，因此概率为：
 *
 *     Pj = 2 * (j - 1) / [ m * (m - 1) ]
 *
 * --------------------------------------------------
 * 【最终答案公式】
 *
 *     Ans = sum_{j=2}^{m} Pj * Fj
 *
 * 即：
 *
 *     Ans = sum_{j=2}^{m}
 *           [ 2(j-1) / (m(m-1)) ] * max(a3, Gj)
 *
 * 其中：
 *
 *     Gj = [ (j-2) * bj + sum_{k=j+1}^{m} bk ] / (m-2)
 *
 * --------------------------------------------------
 * 【如何把数学公式翻译成代码】
 *
 * 1. 读入初始 3 个值，排序，得到最大值 mx = a3
 * 2. 读入剩余 m=n-3 个值，排序为升序数组 b
 * 3. 预处理 b 的后缀和 suf[i]
 *    这样：
 *
 *        sum_{k=j+1}^{m} bk
 *
 *    就能在 O(1) 时间取到
 * 4. 枚举 j=2..m，按公式计算：
 *    - Gj
 *    - Pj
 *    - Fj = max(mx, Gj)
 *    - 累加答案
 *
 * --------------------------------------------------
 * 【下标映射注意事项】
 *
 * 数学里：
 *     b1, b2, ..., bm
 *
 * 代码里若用 0 下标：
 *     b[0], b[1], ..., b[m-1]
 *
 * 那么：
 *     数学里的 bj  <=> 代码里的 b[j-1]
 *
 * 所以循环写：
 *
 *     for (int j = 2; j <= m; j++) {
 *         int idx = j - 1;
 *         // b[idx] 就是数学中的 bj
 *     }
 *
 * --------------------------------------------------
 * 【样例理解】
 *
 * 样例：
 *     n = 8
 *     初始 3 个：3 4 7
 *     剩余池：1 2 3 5 8
 *
 * 初始排序后：
 *     a1=3, a2=4, a3=7
 *
 * 所以前两个一定刷，保留的初始最大值是：
 *     mx = 7
 *
 * 剩余池排序：
 *     b = [1,2,3,5,8], m=5
 *
 * 枚举 j=2..5 后可算得最终期望为：
 *     7.400
 *
 * --------------------------------------------------
 * 【复杂度分析】
 *
 * - 排序：O(n log n)
 * - 后缀和：O(n)
 * - 枚举所有 j：O(n)
 *
 * 所以总复杂度为：
 *
 *     O(n log n)
 *
 * 对于 n <= 1e5 完全可行。
 *
 * --------------------------------------------------
 * 【这题最值得吸收的营养】
 *
 * 1. 先做策略化简，再谈期望
 *    不是一上来就硬列式子，而是先判断：
 *    - 哪些操作一定做？
 *    - 哪些信息其实不重要？
 *
 * 2. 期望题里，状态压缩特别重要
 *    这题本来看起来要考虑“前两次抽到哪两个值”，
 *    但最后只需要关心“较大值是谁”。
 *
 * 3. 一旦数学建模完成，代码本质上只是翻译
 *    真正的创造性发生在代码之前。
 */

#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    // 读入总符文数 n
    int n;
    cin >> n;

    // 读入初始 3 个符文
    vector<long long> a(3);
    for (int i = 0; i < 3; i++)
    {
        cin >> a[i];
    }

    // 剩余符文数
    int m = n - 3;

    // 读入剩余符文池
    vector<long long> b(m);
    for (int i = 0; i < m; i++)
    {
        cin >> b[i];
    }

    // 初始 3 个排序，方便取出最大值 a3
    sort(a.begin(), a.end());

    // 剩余池排序，后面公式要求按升序处理
    sort(b.begin(), b.end());

    // 初始 3 个中保留下来的最大值
    long long mx = a[2];

    // 后缀和：suf[i] = b[i] + b[i+1] + ... + b[m-1]
    // 便于 O(1) 计算 “所有比 bj 大的值之和”
    vector<long long> suf(m + 1, 0);
    for (int i = m - 1; i >= 0; i--)
    {
        suf[i] = suf[i + 1] + b[i];
    }

    double ans = 0.0;

    // 枚举“前两次刷新后抽到的较大值是 bj”
    // 数学下标 j: 2..m
    for (int j = 2; j <= m; j++)
    {
        int idx = j - 1; // 代码中的 b[idx] 对应数学里的 bj

        // Gj = [ (j-2) * bj + sum_{k=j+1}^{m} bk ] / (m-2)
        double Gj = ((double)(j - 2) * b[idx] + suf[idx + 1]) / (m - 2);

        // Pj = 2 * (j-1) / [m * (m-1)]
        double Pj = 2.0 * (j - 1) / (1.0 * m * (m - 1));

        // 在“较大值是 bj”条件下的最优最终收益
        double Fj = max((double)mx, Gj);

        // 累加期望
        ans += Pj * Fj;
    }

    // 输出保留三位小数
    cout << fixed << setprecision(3) << ans << '\n';

    return 0;
}

/**
 * 【最容易错的地方】
 *
 * 1. 不要忘记初始 3 个里，只有最大值 a3 保留，另外两个一定要刷
 *
 * 2. 这题枚举的不是“前两次抽到哪两个”，而是“前两次抽到的较大值是谁”
 *
 * 3. 数学里的 bj 对应代码里 b[j-1]
 *
 * 4. sum_{k=j+1}^{m} bk 对应代码里 suf[idx+1]
 *
 * 5. 概率公式是：
 *      2*(j-1)/(m*(m-1))
 *    不是别的形式
 *
 * 6. 一定要用 double，不能整除
 *
 * 【这题的最终总结】
 *
 * 这题难点不在代码，而在于先把问题建模清楚。
 * 一旦最优策略和期望公式推出来，后面的实现本质上只是把数学表达式翻译成代码。
 */