/**
 * ======================================================================================
 * @file    Twelvefold_Way_Stirling.cpp
 * @author  Reese_fan (NJU-CS)
 * @date    2026-03-19
 * @brief   组合数学与递推：第二类斯特林数 (Stirling Numbers of the Second Kind)
 * ======================================================================================
 *
 * 【数学模型：n个不同球放入m个相同盒子（无空盒）】
 * 1. 物理定义：将一个包含 n 个不同元素的集合拆分为 m 个非空且互不相交的子集的方案数。
 * 2. 递推逻辑：考虑第 n 个球的去向：
 *    - 方案 A (自立门户)：第 n 个球单独占领一个新盒子。
 *      此时前 n-1 个球占领了 m-1 个盒子。方案数为：S(n-1, m-1)
 *    - 方案 B (随大流)：第 n 个球加入已经有球的 m 个盒子中的任意一个。
 *      由于球是不同的，加入不同的盒子是不同的方案。方案数为：m * S(n-1, m)
 * 3. 递推公式：S(n, m) = S(n-1, m-1) + m * S(n-1, m)
 *
 * ======================================================================================
 * 【工程避坑指南：南大计科拔尖班提醒】
 *
 * 1. 逗号运算符陷阱：
 *    - 错误：cin >> n, m; (等价于 cin >> n; m; m 根本没读入)
 *    - 正确：cin >> n >> m;
 *
 * 2. 预处理意识 (Precomputation)：
 *    - 对于这种 O(N^2) 规模的递推，习惯性地在 main 函数外进行全局填表。
 *    - 这样即便题目有 T 组询问，每次查询也仅需 O(1)。
 *
 * 3. 模运算安全性：
 *    - 必须“步步取模”。
 *    - 在进行 j * S[i-1][j] 乘法时，必须强转 (long long) 以防在取模前溢出。
 * ======================================================================================
 */

#include <iostream>
#include <vector>

using namespace std;

// 使用常数定义，方便维护且防止手抖写错
typedef long long ll;
const int MOD = 998244353;
const int MAXN = 505;

// 全局静态数组，自动初始化为 0，且位于静态区，不占系统栈空间
ll S[MAXN][MAXN];

/**
 * @brief 动态规划填表法计算第二类斯特林数
 */
void precompute_stirling()
{
    // 基础边界
    for (int i = 1; i < MAXN; i++)
    {
        S[i][i] = 1; // n 个不同球放入 n 个相同盒子，只有 1 种分法
        S[i][1] = 1; // n 个不同球放入 1 个相同盒子，只有 1 种分法
    }

    // 状态转移过程
    for (int i = 2; i < MAXN; i++)
    { // i 代表球数 (n)
        for (int j = 2; j < i; j++)
        { // j 代表盒子数 (m)
            /**
             * 核心公式：S[i][j] = S[i-1][j-1] + j * S[i-1][j]
             * 注意：先计算 j * S[i-1][j] 并取模，再加上 S[i-1][j-1] 再取模
             */
            ll part_a = S[i - 1][j - 1];
            ll part_b = (ll)j * S[i - 1][j] % MOD;
            S[i][j] = (part_a + part_b) % MOD;
        }
    }
}

int main()
{
    // 性能优化：解除 C++ 输入输出流与 C 标准库的同步
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    // 1. 初始化表：这是所有 DP 操作的序曲
    precompute_stirling();

    int n, m;
    // 2. 修正后的读入：确保 n 和 m 都被正确输入
    if (cin >> n >> m)
    {

        // 3. 边界安全检查
        // 若盒子比球多，且要求无空盒，方案数为 0
        if (m > n || m <= 0)
        {
            cout << 0 << endl;
        }
        else
        {
            // 4. O(1) 极速响应
            cout << S[n][m] << endl;
        }
    }

    return 0;
}

/**
 * 【学长深度总结：从做题到建模】
 *
 * 1. 递归 vs 递推：
 *    递归是“由大化小”，递推是“由小聚大”。在处理有模运算且规模明确的问题时，
 *    递推（填表）不容易爆栈，且逻辑极其清晰。
 *
 * 2. 为什么盒子是“相同”的？
 *    如果盒子是“不同”的，那么结果需要再乘以 m! (m的阶乘)。
 *    这就是组合数学中经典的“球盒模型八种变换”之一。
 *
 * 3. 职业习惯：
 *    养成在 main 之前通过 typedef 定义 ll，通过 const 定义 MAXN。
 *    这种“常量化”编程风格能极大减少大规模项目中的 Bug 率。
 * ======================================================================================
 */