/**
 * @file StringHash_DictionaryQuery.cpp
 * @brief 字符串哈希：统计查询串中有多少个出现在字典中
 *
 * 【题目内容】
 * 给定一个由 n 个字符串构成的字典，再给出 m 个待查询字符串。
 * 需要统计：
 *
 *     这 m 个待查询字符串中，有多少个出现在字典里。
 *
 * 题目保证：
 * - 字符串只包含小写字母
 * - 字符串长度不超过 15
 *
 * --------------------------------------------------
 * 【问题本质】
 *
 * 这题本质上是在做：
 *
 *     “字符串集合判存在”
 *
 * 也就是：
 * - 先把字典中的每个字符串存起来
 * - 再判断每个查询串是否在集合中出现过
 *
 * --------------------------------------------------
 * 【为什么可以用字符串哈希】
 *
 * 直接存字符串当然也可以，
 * 但字符串比较本身比整数比较更慢。
 *
 * 所以可以把每个字符串映射成一个整数哈希值，再进行集合查找。
 *
 * 字符串哈希常见写法：
 *
 *     h = h * base + c
 *
 * 再对一个大质数 mod 取模。
 *
 * --------------------------------------------------
 * 【哈希设计】
 *
 * 设：
 * - base = 13331
 * - mod  = 1000000007
 *
 * 对字符串 s，从左到右计算：
 *
 *     h = (h * base + c) % mod
 *
 * 其中 c 是字符的 ASCII 值。
 *
 * 最终 h 就是整个字符串的哈希值。
 *
 * --------------------------------------------------
 * 【为什么不能把 base 和 mod 设成同一个数】
 *
 * 如果写成：
 *
 *     h = (p * h + c) % p
 *
 * 那么：
 *
 *     (p * h) % p = 0
 *
 * 最后哈希就会退化成只和最后一个字符有关。
 *
 * 所以：
 * - base 和 mod 必须分开
 * - base 用来滚动构造哈希
 * - mod 用来取模防止数过大
 *
 * --------------------------------------------------
 * 【为什么 insert 不能写在字符循环内部】
 *
 * 如果在遍历字符串字符时，每次都把当前 h 插入集合，
 * 那实际插进去的是：
 *
 *     这个字符串的所有前缀哈希
 *
 * 而题目要判断的是：
 *
 *     完整字符串是否出现过
 *
 * 所以必须等整串哈希算完之后，再插入一次。
 *
 * --------------------------------------------------
 * 【数据结构选择】
 *
 * 这里只需要判断“某个哈希值是否存在”，
 * 不需要有序性，也不需要统计次数。
 *
 * 所以可以使用：
 *
 *     unordered_set<long long>
 *
 * --------------------------------------------------
 * 【算法流程】
 *
 * 1. 读入 n 和 m
 * 2. 依次读入字典中的 n 个字符串
 *    - 计算整串哈希
 *    - 插入哈希集合
 * 3. 依次读入 m 个查询字符串
 *    - 计算整串哈希
 *    - 如果该哈希在集合中存在，则答案加一
 * 4. 输出答案
 *
 * --------------------------------------------------
 * 【复杂度分析】
 *
 * 设所有字符串总长度为 L。
 *
 * - 建表：O(L)
 * - 查询：O(L)
 *
 * 哈希集合查找均摊 O(1)。
 *
 * 总体复杂度：
 *
 *     O(L)
 *
 * --------------------------------------------------
 * 【这题最值得吸收的营养】
 *
 * 1. 字符串哈希的基本写法：
 *    h = h * base + c
 *
 * 2. base 和 mod 的作用要分清
 *
 * 3. “判存在”类题，本质是：
 *    把对象映射后放进集合，再查集合
 *
 * 4. 这题也可以自然过渡到 Trie（字典树）学习：
 *    哈希是“整串映射成一个值”，
 *    Trie 是“按字符一层层走”
 *
 * --------------------------------------------------
 * 【一句话总结】
 *
 * 这题本质上是“字符串集合判存在”，
 * 可以用字符串哈希把整串映射成整数，再用哈希集合统计查询串是否出现。
 */

#include <bits/stdc++.h>
using namespace std;

typedef long long ll;

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n, m;
    cin >> n >> m;

    const ll base = 13331;
    const ll mod = 1000000007;

    // 存储字典中所有字符串的哈希值
    unordered_set<ll> st;

    // 读入字典字符串，计算整串哈希后插入集合
    while (n--)
    {
        string s;
        cin >> s;

        ll h = 0;
        for (char c : s)
        {
            h = (h * base + c) % mod;
        }

        st.insert(h);
    }

    // 统计查询串中有多少个在字典里出现过
    ll res = 0;
    while (m--)
    {
        string t;
        cin >> t;

        ll h = 0;
        for (char c : t)
        {
            h = (h * base + c) % mod;
        }

        if (st.count(h))
        {
            res++;
        }
    }

    cout << res;
    return 0;
}