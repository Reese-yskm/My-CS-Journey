/**
 * @file Take_Numbers_Game.cpp
 * @brief 区间动态规划 (Interval DP) - 取数博弈问题
 *
 * 【题目描述】
 * 2n 个数排成一排，kk 先手，ff 后手。每次只能取开头或末尾。
 * 两人都采取最优策略，求最终谁赢。
 *
 * 【核心建模：得分差建模】
 * 如果我们分别记录 kk 和 ff 的总分，状态转移会非常复杂。
 * 核心技巧：定义 dp[i][j] 为在剩下区间为 [i, j] 时，【当前取数的人】相对于【对手】能获得的最大“净胜分”。
 *
 * 净胜分 = 我的得分 - 你的得分
 * 目标：最大化这个差值。
 */

#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;

// 全局变量，2n <= 2000
int dp[2005][2005];

int main()
{
    int n;
    if (!(cin >> n))
        return 0;
    int total_len = 2 * n;
    vector<int> num(total_len);
    for (int i = 0; i < total_len; i++)
        cin >> num[i];

    /**
     * 【重点讲解：推导逻辑】
     *
     * 面对区间 [i, j]，我有两种选择：
     * 1. 选左端的数 num[i]：
     *    我的分增加了 num[i]。接下来轮到对方在区间 [i+1, j] 拿走他的最大净胜分 dp[i+1][j]。
     *    相对于我来说，我的净胜分就是：num[i] - dp[i+1][j]。
     *
     * 2. 选右端的数 num[j]：
     *    我的分增加了 num[j]。接下来轮到对方在区间 [i, j-1] 拿走他的最大净胜分 dp[i][j-1]。
     *    相对于我来说，我的净胜分就是：num[j] - dp[i][j-1]。
     *
     * 状态转移方程：
     * dp[i][j] = max(num[i] - dp[i+1][j], num[j] - dp[i][j-1])
     */

    // --- 状态初始化 ---
    // 当区间长度为 1 时，直接拿走那个数，净胜分就是该数本身
    for (int i = 0; i < total_len; i++)
    {
        dp[i][i] = num[i];
    }

    /**
     * 【重点讲解：为什么要这样写循环？】
     *
     * 这种循环方式称为“区间长度递增法”。
     *
     * 1. 为什么不能直接按 i 和 j 嵌套？
     *    因为计算大区间 [i, j] 的答案时，依赖于小区间 [i+1, j] 或 [i, j-1] 的结果。
     *    如果直接按 i, j 循环，计算 dp[0][5] 时，dp[1][5] 可能还没算出来。
     *
     * 2. 逻辑顺序：
     *    我们必须保证：在算长度为 len 的区间之前，所有长度为 len-1 的子问题都已经算完了。
     *    这就像盖房子，必须先打好小规模的“地基”，才能往上盖。
     */
    for (int len = 2; len <= total_len; len++)
    { // 第一层：枚举区间长度
        for (int i = 0; i <= total_len - len; i++)
        {                        // 第二层：枚举起始位置
            int j = i + len - 1; // 根据起点和长度算出终点

            // 当前面对 [i, j] 的人采取最优策略
            dp[i][j] = max(num[i] - dp[i + 1][j], num[j] - dp[i][j - 1]);
        }
    }

    // 最后看整个区间的净胜分。因为是 kk 先手，
    // dp[0][total_len - 1] 就是 kk 最终相对于 ff 的得分差。
    if (dp[0][total_len - 1] > 0)
    {
        cout << "kk" << endl;
    }
    else
    {
        cout << "ff" << endl;
    }

    return 0;
}

/**
 * 【关键核心知识点 & 注意事项】
 *
 * 1. 博弈论中的“Minimax”思想：
 *    在 DP 方程中使用“减法”（num[i] - dp[...]）非常巧妙。它体现了：
 *    “我的最优决策 = 当前拿到的分 - 对方在剩下部分的最优表现”。
 *
 * 2. 区间 DP 的标准模板：
 *    - 初始化长度为 1（或 2）的基础状态。
 *    - 第一层循环：len (区间长度)。
 *    - 第二层循环：i (左端点)。
 *    - 计算出 j (右端点)。
 *    - 时间复杂度：O(N^2)，空间复杂度：O(N^2)。
 *
 * 3. 为什么贪心（只看两头大的）会错？
 *    贪心是短视的。DP 通过填表，实际上是模拟了“穷举所有未来可能性”后得出的最佳第一步。
 *
 * 4. 脑筋急转弯（补充知识）：
 *    对于这类两端取数且总数为偶数的游戏，先手其实有一种“奇偶位必胜策略”，可以做到 O(N)。
 *    但学会这个 DP 模型，你可以处理总数为奇数、或者每次可以取 3 个数等更复杂的变化。
 */