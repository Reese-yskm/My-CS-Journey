/**
 * @file TwoSum_Hash.cpp
 * @brief 哈希表：在数组中找到和为 target 的两个数下标
 *
 * 【题目内容】
 * 给定一个整数数组 nums 和一个目标值 target，
 * 在数组中找出两个数，使它们的和等于 target，
 * 并输出它们的数组下标。
 *
 * 题目保证：
 * - 一定存在这样的两个数
 * - 同一个元素不能重复使用
 *
 * --------------------------------------------------
 * 【问题本质】
 *
 * 这题要求：
 *
 *     对每个 nums[i]，快速判断 target - nums[i] 是否已经出现过
 *
 * 朴素做法是双重循环枚举两数：
 *
 *     O(n^2)
 *
 * 当 n <= 1e5 时显然太慢。
 *
 * --------------------------------------------------
 * 【哈希表优化思想】
 *
 * 遍历数组时，维护一个哈希表：
 *
 *     value -> index
 *
 * 表示某个值之前出现过，并记录它的下标。
 *
 * 当前遍历到 nums[i] = x 时：
 *
 * - 设补数为 y = target - x
 * - 如果 y 已经在哈希表中，
 *   说明之前某个位置 j 满足：
 *
 *       nums[j] + nums[i] = target
 *
 *   直接输出 j 和 i 即可
 *
 * - 如果 y 不在哈希表中，
 *   就把当前值 x 及其下标 i 放进哈希表
 *
 * --------------------------------------------------
 * 【为什么要“边遍历边存”】
 *
 * 如果先把所有值都存进哈希表，再去找，
 * 那当存在重复值时容易把前面的下标覆盖掉，
 * 甚至可能输出同一个下标两次。
 *
 * 而边遍历边存的好处是：
 *
 * - 哈希表里只记录“之前出现过的元素”
 * - 当前元素只和之前元素配对
 * - 天然保证两个下标不同
 *
 * --------------------------------------------------
 * 【算法流程】
 *
 * 1. 读入 n 和 target
 * 2. 依次读入每个 nums[i]
 * 3. 对当前值 x：
 *    - 判断 target - x 是否已在哈希表中
 *    - 如果在，输出对应下标和 i
 *    - 否则记录 x -> i
 *
 * --------------------------------------------------
 * 【数据结构选择】
 *
 * 使用：
 *
 *     unordered_map<long long, int>
 *
 * 存：
 *
 *     值 -> 下标
 *
 * --------------------------------------------------
 * 【复杂度分析】
 *
 * - 每个元素只处理一次
 * - 哈希表查找 / 插入均摊 O(1)
 *
 * 总复杂度：
 *
 *     O(n)
 *
 * --------------------------------------------------
 * 【这题最值得吸收的营养】
 *
 * 1. Two Sum 是哈希表最经典的入门题
 *
 * 2. 一看到“快速判断某个值是否之前出现过”，
 *    就要想到哈希表
 *
 * 3. “边遍历边存”是非常经典的模式：
 *    - 当前元素查之前
 *    - 查不到再把自己放进去
 *
 * --------------------------------------------------
 * 【一句话总结】
 *
 * 遍历数组时，用哈希表记录之前出现过的值及其下标；
 * 对当前数 x，只需检查 target - x 是否已出现过即可。
 */

#include <bits/stdc++.h>
using namespace std;

typedef long long ll;

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n;
    ll target;
    cin >> n >> target;

    unordered_map<ll, int> pos;

    for (int i = 0; i < n; i++)
    {
        ll x;
        cin >> x;

        ll need = target - x;

        // 如果补数之前出现过，直接输出答案
        if (pos.count(need))
        {
            cout << pos[need] << " " << i;
            return 0;
        }

        // 否则记录当前值及其下标
        pos[x] = i;
    }

    return 0;
}