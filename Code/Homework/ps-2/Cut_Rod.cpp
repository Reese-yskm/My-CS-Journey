#include <bits/stdc++.h>
using namespace std;

// s[j] 表示：长度为 j 的钢条，在最优切割方案中，第一段切多长
// 例如 s[7] = 1，表示长度为 7 时，最优方案的第一段长度是 1
int s[10005];

// r[j] 表示：长度为 j 的钢条能够获得的最大收益
// 也就是 CLRS 中的 r_j
int r[10005];

// p[i] 表示：长度为 i 的钢条直接出售的价格
// 也就是价格表中的 p_i
int p[10005];

// 自底向上求解钢条切割问题
// 返回长度为 n 的钢条的最大收益
int cut_rod(int n)
{
    // 初始化 r 数组
    // r[0] = 0 表示长度为 0 的钢条收益为 0
    // 其他 r[i] 后面会按长度从小到大依次计算
    for (int i = 0; i <= n; i++)
    {
        r[i] = 0;
    }

    // 外层循环：依次计算 r[1], r[2], ..., r[n]
    // j 表示当前正在求解的钢条长度
    for (int j = 1; j <= n; j++)
    {
        // q 表示当前长度 j 的最大收益候选值
        // 注意：q 必须在每个 j 内部重新初始化
        // 因为每个 r[j] 都是一个独立的子问题
        int q = INT_MIN;

        // 枚举第一段成品钢条的长度 i
        // 如果第一段长度为 i，那么这一段收益为 p[i]
        // 剩余长度为 j - i，其最优收益已经提前算好，即 r[j - i]
        for (int i = 1; i <= j; i++)
        {
            // 当前方案的收益：
            // 第一段直接卖 p[i]
            // 剩余部分按最优方案切割，收益为 r[j - i]
            int value = p[i] + r[j - i];

            // 如果当前方案更优，就更新最大收益
            if (q < value)
            {
                q = value;

                // 记录长度 j 的最优方案中，第一段切成长度 i
                // 后面恢复切割方案时会用到 s[j]
                s[j] = i;
            }
        }

        // 所有第一段长度 i 都枚举完后，q 就是 r[j]
        r[j] = q;
    }

    // 返回长度为 n 的钢条的最大收益
    return r[n];
}

// 根据 s 数组打印最优切割方案
void print_pro(int n)
{
    // 只要剩余长度还大于 0，就继续切
    while (n > 0)
    {
        // s[n] 表示当前长度 n 的最优方案中，第一段长度
        cout << s[n] << " ";

        // 切掉这一段后，剩余长度减少 s[n]
        n -= s[n];
    }
}

int main()
{
    int n;
    cin >> n;

    // 输入价格表 p[1..n]
    // p[i] 表示长度为 i 的钢条直接出售的价格
    for (int i = 1; i <= n; i++)
    {
        cin >> p[i];
    }

    // 输出最大收益
    cout << cut_rod(n) << "\n";

    // 输出一种最优切割方案
    print_pro(n);

    return 0;
}