/**
 * @file Dynamic_Median.cpp
 * @brief 双堆维护动态中位数
 *
 * 【题目内容】
 * 给定一个长度为 n 的整数序列，按输入顺序依次插入。
 * 每插入一个新数后，都要立刻输出“当前所有已插入数字的中位数”。
 *
 * 例如：
 * - 当前共有奇数个数时，中位数就是排好序后正中间那个数
 * - 当前共有偶数个数时，中位数是中间两个数的平均值
 *
 * 数据范围较大，不能每次插入后都重新排序。
 *
 * --------------------------------------------------
 * 【为什么朴素做法会超时】
 *
 * 最朴素的想法是：
 * - 每读入一个数，就插入数组
 * - 然后重新排序，或者插入到有序位置
 * - 最后取中位数
 *
 * 这样做的复杂度至少会达到：
 * - 重新排序：O(n^2 log n)
 * - 线性插入保持有序：O(n^2)
 *
 * 当 n <= 1e5 时，这样肯定会超时。
 *
 * 所以必须寻找一种能“动态维护中位数”的数据结构。
 *
 * --------------------------------------------------
 * 【核心思路：两个堆】
 *
 * 我们不需要维护“整个序列完全有序”，
 * 只需要维护“中间分界线”附近的信息即可。
 *
 * 将当前所有数分成两部分：
 *
 * 1. 左边一半：较小的一半
 * 2. 右边一半：较大的一半
 *
 * 为了快速取到边界元素：
 *
 * - 左边用大顶堆 left
 *   这样可以 O(1) 取到左半边最大值
 *
 * - 右边用小顶堆 right
 *   这样可以 O(1) 取到右半边最小值
 *
 * --------------------------------------------------
 * 【两个堆维护的两个不变式】
 *
 * 为了保证中位数始终能正确取出，我们一直维护：
 *
 * 不变式 1：大小平衡
 * - left.size() == right.size()
 *   或
 * - left.size() == right.size() + 1
 *
 * 也就是说：
 * - 左边要么和右边一样多
 * - 要么左边比右边多 1 个
 *
 * 为什么这么设计？
 * - 如果当前总数为奇数，中位数直接取 left.top()
 * - 如果当前总数为偶数，中位数取 left.top() 和 right.top() 的平均值
 *
 * 不变式 2：顺序正确
 * - 左边所有元素 <= 右边所有元素
 *
 * 只要这个条件成立，那么：
 * - left.top() 就是左半边最大值
 * - right.top() 就是右半边最小值
 * - 中位数就只由这两个边界值决定
 *
 * --------------------------------------------------
 * 【插入新元素时怎么做】
 *
 * 设新读入的数为 x。
 *
 * 第一步：先按值决定放哪边
 * - 如果 left 为空，或者 x <= left.top()
 *   说明 x 更像“较小的一半”，先放 left
 * - 否则，放 right
 *
 * 第二步：再平衡
 * 插入后，两个堆的大小可能不满足不变式 1。
 *
 * 只会有两种失衡情况：
 *
 * 1. left 太多了
 *    即 left.size() > right.size() + 1
 *    这时把 left.top() 搬到 right
 *
 * 2. right 比 left 还多
 *    即 right.size() > left.size()
 *    这时把 right.top() 搬到 left
 *
 * 注意：
 * 每次只插入一个数，所以失衡最多只会差 1 个边界之外的元素，
 * 因此再平衡时最多移动一次堆顶即可。
 *
 * --------------------------------------------------
 * 【为什么这样能保证“左边全 <= 右边全”】
 *
 * 这是这题最关键的理解点之一。
 *
 * 因为：
 * - left 是大顶堆，left.top() 是左边最大值
 * - right 是小顶堆，right.top() 是右边最小值
 *
 * 所以只要维护住：
 *
 *     left.top() <= right.top()
 *
 * 就能推出：
 *
 *     左边所有元素 <= 右边所有元素
 *
 * 原因是：
 * - 左边任意元素 <= 左边最大值
 * - 右边任意元素 >= 右边最小值
 *
 * 插入新元素时：
 * - 先按值分边，避免明显放错区域
 * - 再通过搬堆顶调整边界
 *
 * 而再平衡时搬的恰好是“边界元素”：
 * - left.top()：左边最大值
 * - right.top()：右边最小值
 *
 * 所以顺序不会被破坏。
 *
 * --------------------------------------------------
 * 【中位数如何输出】
 *
 * 设已经读入了 i 个数。
 *
 * 1. 如果 i 为奇数
 *    根据我们的大小维护规则，left 一定比 right 多 1 个，
 *    所以中位数就是：
 *
 *        left.top()
 *
 * 2. 如果 i 为偶数
 *    left 和 right 一样多，
 *    中位数就是：
 *
 *        (left.top() + right.top()) / 2
 *
 * 本题代码中采用整数除法，表示输出整数中位数。
 * 如果题目要求真实平均值，则需要改成浮点除法。
 *
 * --------------------------------------------------
 * 【复杂度分析】
 *
 * 每插入一个元素：
 * - 插入堆：O(log n)
 * - 再平衡：O(log n)
 * - 取中位数：O(1)
 *
 * 总复杂度：
 *
 *     O(n log n)
 *
 * 能通过 n <= 1e5 级别的数据。
 *
 * --------------------------------------------------
 * 【这题最值得吸收的营养】
 *
 * 1. 动态维护中位数，不需要维护整个序列完全有序，
 *    只需要维护“左右两半”和“中间边界”。
 *
 * 2. 两个堆是经典模型：
 *    - 左边大顶堆
 *    - 右边小顶堆
 *
 * 3. 数据结构题真正难的常常不是代码，
 *    而是先把维护规则想清楚：
 *    - 维护什么状态
 *    - 满足什么不变式
 *    - 插入后如何恢复不变式
 *
 * 4. 一旦结构想清楚，代码本质上就是把这个结构翻译出来。
 */

#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n;
    cin >> n;

    // left：左半边，维护较小的一半，使用大顶堆
    priority_queue<int> left;

    // right：右半边，维护较大的一半，使用小顶堆
    priority_queue<int, vector<int>, greater<int>> right;

    for (int i = 1; i <= n; i++)
    {
        int x;
        cin >> x;

        // ----------------------------
        // 第一步：先按值决定放哪边
        // ----------------------------
        // 如果左堆为空，或者 x 不大于左堆最大值，
        // 说明 x 更适合放在左边
        if (left.empty() || x <= left.top())
        {
            left.push(x);
        }
        else
        {
            // 否则放在右边
            right.push(x);
        }

        // ----------------------------
        // 第二步：再平衡两个堆的大小
        // ----------------------------
        // 目标始终保持：
        // 1. left.size() == right.size()
        // 2. 或 left.size() == right.size() + 1

        // 如果左边太多了，就把左边最大值搬到右边
        if (left.size() > right.size() + 1)
        {
            right.push(left.top());
            left.pop();
        }
        // 如果右边比左边还多，就把右边最小值搬到左边
        else if (right.size() > left.size())
        {
            left.push(right.top());
            right.pop();
        }

        // ----------------------------
        // 第三步：输出当前中位数
        // ----------------------------
        if (i % 2 == 1)
        {
            // 当前总数为奇数
            // 按维护规则，left 一定比 right 多 1 个
            // 所以中位数就是 left.top()
            cout << left.top() << '\n';
        }
        else
        {
            // 当前总数为偶数
            // 两个堆一样多
            // 中位数是两个边界值的平均值
            cout << (left.top() + right.top()) / 2 << '\n';
        }
    }

    return 0;
}

/**
 * 【实现时最容易错的地方】
 *
 * 1. 不要写成一个 while 再平衡但忘记更新 size。
 *    由于每次只插入一个数，失衡最多只差 1，
 *    所以这里直接 if / else if 一次搬运就够了。
 *
 * 2. left 是大顶堆，right 是小顶堆，不要写反。
 *
 * 3. 中位数输出时：
 *    - 奇数：left.top()
 *    - 偶数：(left.top() + right.top()) / 2
 *
 * 4. 如果题目要求小数中位数，要把偶数情况改成浮点除法。
 *
 * 【一句话总结】
 *
 * 这题是“两个堆动态维护中位数”的经典模型：
 * 左大顶堆维护较小一半，右小顶堆维护较大一半，
 * 始终保持大小平衡和边界有序，中位数就能 O(1) 取出。
 */