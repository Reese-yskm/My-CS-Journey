/**
 * @file P4017_Food_Chain.cpp
 * @brief 洛谷 P4017 最大食物链计数 - 拓扑排序 + 图上动态规划
 *
 * 【题目复盘】
 * 1. 核心需求：在一个有向无环图 (DAG) 中，寻找从所有“源点”到所有“汇点”的路径总数。
 * 2. 关键定义：“最大食物链”指的是从生产者（入度为 0）到顶级捕食者（出度为 0）的完整路径。
 * 3. 约束条件：数据量大 (N=5000, M=5*10^5)，必须在 O(V+E) 内完成；结果需模 80112002。
 *
 * 【建模思考：为何拓扑排序是“有序化”神器？】
 *
 * 1. 解决依赖混乱：
 *    在图上直接求路径数，难点在于不知道“谁先谁后”。拓扑排序通过“入度”控制，
 *    保证了当我们处理节点 u 时，所有指向 u 的节点（即 u 的所有食物）都已经计算完毕了。
 *
 * 2. 流量递推逻辑（DP）：
 *    状态定义：f[i] 表示从生产者到达生物 i 的食物链条数。
 *    状态转移：f[v] = Σ f[u] （其中 u 是 v 的食物，即存在边 u -> v）。
 *    物理意义：到达 v 的路径数，等于所有能到达它“食物”的路径数之和（加法原理）。
 */

#include <iostream>
#include <vector>
#include <queue>

using namespace std;

// --- 全局变量与常量 ---
const int MAXN = 5005;
const int MOD = 80112002;

vector<int> adj[MAXN]; // 邻接表：存“谁被谁吃”
int in_degree[MAXN];   // 入度：衡量有多少种食物
int out_degree[MAXN];  // 出度：衡量有多少种天敌
long long f[MAXN];     // DP数组：记录到达该节点的路径数

int main()
{
    // 1. 优化 I/O 效率
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n, m;
    if (!(cin >> n >> m))
        return 0;

    // 2. 建立图并统计度数
    for (int i = 0; i < m; i++)
    {
        int u, v;
        cin >> u >> v;
        adj[u].push_back(v); // u 被 v 吃
        out_degree[u]++;     // u 有了天敌
        in_degree[v]++;      // v 有了食物
    }

    // 3. 拓扑排序初始化：寻找“能量源头”
    queue<int> q;
    for (int i = 1; i <= n; i++)
    {
        if (in_degree[i] == 0)
        {
            f[i] = 1;  // 生产者本身作为路径起点，计为 1
            q.push(i); // 入度为 0 的点最先进入队列
        }
    }

    // 4. 拓扑排序主循环：将复杂的图转化为有序的计算
    while (!q.empty())
    {
        int u = q.front();
        q.pop();

        for (int v : adj[u])
        {
            // 【核心 DP 转移】：能量向下流动，累加路径数
            // 重点：逢加必模，防止中间过程溢出
            f[v] = (f[v] + f[u]) % MOD;

            // “剥洋葱”：切断 u -> v 这条边
            in_degree[v]--;
            if (in_degree[v] == 0)
            {
                // 当 v 的所有前驱节点都处理完了，说明 v 的路径数已收齐
                q.push(v);
            }
        }
    }

    // 5. 统计结果：寻找“路径尽头”
    long long ans = 0;
    for (int i = 1; i <= n; i++)
    {
        // 只有出度为 0 的生物才是顶级捕食者，代表食物链的终结
        if (out_degree[i] == 0)
        {
            ans = (ans + f[i]) % MOD;
        }
    }

    cout << ans << endl;

    return 0;
}

/**
 * 【关键避坑与深度总结】
 *
 * 1. 数组下标意识：
 *    图论题目点编号通常是 1~N。循环务必写 for(i=1; i<=n; i++)。
 *    写成 0~n-1 会导致 0 号空跑且漏掉第 N 号。
 *
 * 2. 拓扑排序的“入队时机”：
 *    必须是 in_degree[v] 减到 0 之后才 push。
 *    逻辑：我得把所有的前提条件（所有食物）都算清楚了，我的值才是准确的。
 *
 * 3. 对“最大”的理解：
 *    本题的“最大”指结构完整（源点到汇点）。
 *    线性路径 1->2->3 的结果是 1，而不是 3。因为只有 3 是出度为 0。
 *
 * 4. 总结感悟：
 *    拓扑排序本质上是在 DAG 上进行动态规划的一种【计算顺序】。
 *    它把“错综复杂”降级为“线性递推”。学会了拓扑排序，你就拿到了
 *    处理所有带依赖关系问题的万能钥匙。
 */