/**
 * @file P1596_Lake_Counting.cpp
 * @brief 洛谷 P1596 [USACO10OCT] Lake Counting S - 连通块问题 (DFS/Flood Fill)
 *
 * 【题目描述】
 * 由于最近的降雨，水在农夫约翰的田地里积聚了。田地可以表示为一个 N * M 的矩形 (1 <= N, M <= 100)。
 * 每个方格中要么是水（'W'），要么是干地（'.'）。
 * 一个水塘是由“连通”的水方格组成的。其中一个方格被认为与它的“八个邻居”相邻。
 * 请确定田地里总共有多少个水塘。
 *
 * 【输入格式】
 * 第 1 行：两个整数 N 和 M。
 * 第 2 到 N+1 行：每行 M 个字符，表示田地（'W' 或 '.'）。
 *
 * 【输出格式】
 * 一个整数，表示水塘的总数。
 */

#include <iostream>

using namespace std;

// --- 全局变量 ---
int n, m;
char grid[105][105];    // 存储地图
bool visited[105][105]; // 标记是否访问过，防止重复计算和死循环

// 核心技巧：方向数组
// 题目要求“八个方向”，我们通过偏移量来简化代码，避免写 8 个 if
int dx[8] = {-1, -1, 0, 1, 1, 1, 0, -1};
int dy[8] = {0, 1, 1, 1, 0, -1, -1, -1};

/**
 * @brief 深度优先搜索 (DFS)
 * 作用：从 (x, y) 出发，把与之相连的所有水方格 ('W') 都标记为已访问。
 * 就像在水塘里滴入一滴红墨水，红墨水会顺着连通的水流把整个水塘染红。
 */
void dfs(int x, int y)
{
    visited[x][y] = true; // 踏入这块水域，标记为已访问

    // 尝试向 8 个方向扩散
    for (int i = 0; i < 8; i++)
    {
        int nx = x + dx[i];
        int ny = y + dy[i];

        // 边界检查：不能走出地图范围
        if (nx >= 0 && nx < n && ny >= 0 && ny < m)
        {
            // 如果是水，且还没被染红（没访问过）
            if (grid[nx][ny] == 'W' && !visited[nx][ny])
            {
                dfs(nx, ny); // 继续递归探索
            }
        }
    }
    // 注意：当 8 个方向都走不通时，函数会自动返回，这就是 DFS 的回溯性质
}

int main()
{
    // 优化输入输出效率
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    // 1. 读取输入
    if (!(cin >> n >> m))
        return 0;
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < m; j++)
        {
            cin >> grid[i][j];
        }
    }

    int res = 0; // 记录水塘数量

    // 2. 遍历地图
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < m; j++)
        {
            // --- 本题核心难点：res++ 的时机 ---
            // 只有当我们发现一块“从未被访问过”的“水 (W)”时，才说明发现了一个新水塘
            if (grid[i][j] == 'W' && !visited[i][j])
            {
                res++;     // 既然是新发现的水塘，计数器 +1
                dfs(i, j); // 立即调用 DFS，把这个水塘连通的所有 W 都标记掉
                           // 这样后续的遍历就不会再把同一个水塘里的其他 W 当成新水塘了
            }
        }
    }

    // 3. 输出结果
    cout << res << endl;

    return 0;
}

/**
 * 【学习总结与核心思考】
 *
 * 1. 为什么 res++ 在 main 的循环里，而不在 DFS 里？
 *    - 核心逻辑：我们要数的是“水塘的数量（连通块个数）”，而不是“水方格的数量”。
 *    - 在 main 函数里，每触发一次 dfs，就代表“消耗”掉了一个完整的连通块。
 *    - 如果把 res++ 放在 DFS 内部，最后统计出的就是所有 'W' 的总个数。
 *
 * 2. Flood Fill（种子填充）算法模型：
 *    - 这种“找到一个点，然后把周围一圈都感染掉”的操作叫 Flood Fill。
 *    - 就像 Windows 画图里的油漆桶工具。
 *
 * 3. 关于 flag 的思考：
 *    - 在原代码尝试中，你可能想通过 flag 判断是否走到了尽头。但在连通块问题中，
 *      DFS 本身就是一个“全搜索”过程。只要进入了 DFS，就说明当前这个连通块是存在的。
 *      DFS 执行完毕后，整个连通块就被“处理”掉了，逻辑非常简洁。
 *
 * 4. 这种“独立思考 + 核心点启发”的学习方式：
 *    - 先尝试写出框架（代码结构、方向数组、边界处理）。
 *    - 遇到“计数时机”这个逻辑卡点时进行讨论。
 *    - 这种深刻的印象比死记硬背模板强百倍！
 */