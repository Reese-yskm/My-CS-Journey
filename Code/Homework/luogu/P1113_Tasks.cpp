/**
 * @file P1113_Tasks.cpp
 * @brief 洛谷 P1113 [USACO02FEB] 杂务 - 拓扑序下的线性 DP
 *
 * 【题目描述】
 * 有 n 个任务，每个任务有持续时间。某些任务必须在另一些任务完成后才能开始。
 * 关键约束：任务 k 的准备工作只可能在任务 1 至 k-1 中。
 * 求完成所有任务的最短总时间。
 *
 * 【建模思考：为何是“最长路”而不是“最短路”？】
 * 1. 虽然题目问“最短完成时间”，但逻辑上任务必须等待所有前置工作结束。
 * 2. 假设任务 C 依赖 A(5min) 和 B(10min)，C 必须在第 10min 才能开始。
 * 3. 结论：这本质上是在求一个 DAG（有向无环图）中的“关键路径”或“最长路径”。
 *
 * 【核心精髓：天然拓扑序】
 * 1. 标准图上 DP：通常需要先跑一遍拓扑排序（入度、队列）来确定计算顺序。
 * 2. 本题特权：题目保证了 pre_id < id，这意味着【输入顺序本身就是合法的拓扑序】。
 * 3. 结果：我们可以边读入边计算，无需建邻接表，无需跑队列。
 */

#include <iostream>
#include <algorithm>

using namespace std;

// f[i] 表示第 i 个任务的【最早完工时刻】
// n 最大 10000
int f[10005];

int main()
{
    // 1. 优化输入输出
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n;
    if (!(cin >> n))
        return 0;

    int total_ans = 0; // 记录所有任务中最晚结束的那个时刻

    // 2. 顺序处理（利用输入即拓扑序的特性）
    // 因为 pre 永远小于 id，当我们读到任务 id 时，它依赖的所有 f[pre] 肯定已经算好了
    for (int i = 1; i <= n; i++)
    {
        int id, length, pre;
        cin >> id >> length;

        int max_pre_finish_time = 0; // 所有前置任务中最晚结束的时间

        // 循环读取前置任务，直到读到 0
        while (cin >> pre && pre != 0)
        {
            max_pre_finish_time = max(max_pre_finish_time, f[pre]);
        }

        /**
         * 【状态转移方程】
         * 当前任务最早完工时刻 = 所有前置工作中最晚的一个完工时间 + 自己持续的时间
         */
        f[id] = max_pre_finish_time + length;

        // 随时更新全局最大值
        total_ans = max(total_ans, f[id]);
    }

    // 3. 输出结果
    cout << total_ans << endl;

    return 0;
}

/**
 * 【避坑指南 & 深度总结】 —— 整理自我的 Debug 历程
 *
 * 1. 变量作用域坑：
 *    - 错误：使用 while(n--) 读入数据，导致后面统计结果时 n 已经变成 0，无法进入循环。
 *    - 修正：使用 for 循环或临时变量，保留 n 的原始值，或在读入时直接维护答案。
 *
 * 2. 初始化陷阱：
 *    - 错误：将 maxn（前置最大时间）初始化为 -1。
 *    - 真相：若一个任务没有前置任务，它应从第 0 时刻开始。初始化为 0 可以完美涵盖所有情况。
 *
 * 3. 逻辑简化：
 *    - 不要写 if(id == 1)，因为任何编号的任务都可能是独立的起点。
 *    - 只要 maxn 初始化为 0，不论是不是 1 号任务，逻辑都是统一的。
 *
 * 4. 深刻领悟：
 *    - 这道题的精髓在于识别“计算顺序”。
 *    - 在图论中，拓扑排序就是为了给 DP 找到那个“从因到果”的轴。
 *    - 只要保证处理某个节点时，它的前驱都已经“定型”，图的问题就会瞬间变得和一维线性 DP 一样简单。
 */